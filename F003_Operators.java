public class F003_Operators {
    
    public static void main(String[] args) {
//      arithmetic operators
        System.out.println("The arithmetic operators are the same as the ones used in math");
        System.out.println("5 + 5 = " + (5 + 5));
        System.out.println("5 - 5 = " + (5 - 5));
        System.out.println("5 * 5 = " + (5 * 5));
        System.out.println("5 / 5 = " + (5 / 5));
        System.out.println("5 % 5 = " + (5 % 5) + "\t (the operator % returns the remainder of the division)");
        
//      assignment operators
//      the assignment operators are used to defining variables
        int a = 5; int b = 2;
        int c = a + b;
//      write c += a; is the same to write c = c + a; || and it works to all the arithmetic operators (-=)(*=)(/=)(%=)

/*      relational operators
        "==" is the standar for equal to
        "!=" is the standar for different to
        ">=" is the standar for greater than
        "<=" is the standar for smaller than
        "&&" is the standar for if statement1 AND statement2 are TRUE, then execute this 
        "||" is the standar for if statement1 OR statement2 are TRUE, then execute this 
        "++" is the standar for addition
        "--" is the standar for subtraction
*/      
    }
}
